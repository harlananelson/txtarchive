def create_test_html_with_images():
    """Create an HTML file with base64-encoded images."""
    # Create a tiny 1x1 red PNG
    import struct
    import zlib

    def create_minimal_png():
        """Create a minimal 1x1 red PNG image."""
        # PNG signature
        signature = b'\x89PNG\r\n\x1a\n'

        # IHDR chunk
        ihdr_data = struct.pack('>IIBBBBB', 1, 1, 8, 2, 0, 0, 0)  # 1x1, 8-bit RGB
        ihdr_crc = zlib.crc32(b'IHDR' + ihdr_data) & 0xffffffff
        ihdr = struct.pack('>I', 13) + b'IHDR' + ihdr_data + struct.pack('>I', ihdr_crc)

        # IDAT chunk (1x1 red pixel)
        raw_data = b'\x00\xff\x00\x00'  # filter byte + RGB
        compressed = zlib.compress(raw_data)
        idat_crc = zlib.crc32(b'IDAT' + compressed) & 0xffffffff
        idat = struct.pack('>I', len(compressed)) + b'IDAT' + compressed + struct.pack('>I', idat_crc)

        # IEND chunk
        iend_crc = zlib.crc32(b'IEND') & 0xffffffff
        iend = struct.pack('>I', 0) + b'IEND' + struct.pack('>I', iend_crc)

        return signature + ihdr + idat + iend

    png_data = create_minimal_png()
    b64_image = base64.b64encode(png_data).decode('ascii')

    return f"""<!DOCTYPE html>
<html>
<body>
<h1>Document with Images</h1>
<p>Here is an inline image:</p>
<img src="data:image/png;base64,{b64_image}" alt="Test Red Pixel" />
<p>And another image:</p>
<img src="data:image/png;base64,{b64_image}" alt="Another Image" />
<p>End of document.</p>
</body>
</html>"""


