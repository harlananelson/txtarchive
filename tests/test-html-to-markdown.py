def test_cli_integration():
    """Test the CLI convert-html subcommand."""
    print("\n=== Testing CLI Integration ===")

    test_dir = Path(tempfile.mkdtemp(prefix="html_cli_test_"))

    try:
        import subprocess

        # Write test HTML
        html_path = test_dir / "test.html"
        html_path.write_text(create_test_html_simple(), encoding='utf-8')

        output_path = test_dir / "test.md"

        cmd = [
            sys.executable, "-m", "txtarchive", "convert-html",
            str(html_path),
            str(output_path),
            "--method", "auto",
            "--no-extract-images"
        ]

        print(f"  Running: {' '.join(cmd)}")
        result = subprocess.run(cmd, capture_output=True, text=True, timeout=30)

        if result.returncode == 0 and output_path.exists():
            content = output_path.read_text()
            print(f"  PASS: CLI conversion succeeded ({len(content)} chars)")
            return True
        else:
            print(f"  FAIL: CLI conversion failed (rc={result.returncode})")
            if result.stderr:
                print(f"  stderr: {result.stderr[:500]}")
            return False

    except Exception as e:
        print(f"  FAIL: CLI test raised: {e}")
        return False

    finally:
        shutil.rmtree(test_dir)


def test_archive_integration():
    """Test HTML conversion during archive creation."""
    print("\n=== Testing Archive Integration ===")

    test_dir = Path(tempfile.mkdtemp(prefix="html_archive_test_"))

    try:
        from txtarchive.packunpack import archive_files

        # Create a directory with an HTML file
        src_dir = test_dir / "source"
        src_dir.mkdir()
        html_path = src_dir / "analysis.html"
        html_path.write_text(create_test_html_simple(), encoding='utf-8')

        # Also add a plain text file
        txt_path = src_dir / "readme.txt"
        txt_path.write_text("This is a readme.", encoding='utf-8')

        # Archive with .html included
        archive_path = test_dir / "archive.txt"
        archive_files(
            directory=src_dir,
            output_file_path=archive_path,
            file_types=['.html', '.txt'],
            llm_friendly=True
        )

        archive_content = archive_path.read_text()

        checks = [
            ("Archive created", archive_path.exists()),
            ("Contains file entry", "analysis.html" in archive_content),
            ("HTML converted to MD", "<h1>" not in archive_content or "# Test Document" in archive_content),
            ("No script tags", "<script>" not in archive_content),
            ("Readme included", "This is a readme" in archive_content),
        ]

        all_passed = True
        for name, passed in checks:
            status = "PASS" if passed else "FAIL"
            print(f"  {status}: {name}")
            if not passed:
                all_passed = False

        return all_passed

    except Exception as e:
        print(f"  FAIL: Archive integration test raised: {e}")
        return False

    finally:
        shutil.rmtree(test_dir)


def run_html_conversion_tests():
    """Run all HTML conversion tests."""
    print("Starting HTML Conversion Tests for TxtArchive")
    print("=" * 50)

    tests = [
        ("Boilerplate Stripping", test_boilerplate_stripping),
        ("Base64 Image Extraction", test_base64_image_extraction),
        ("Pandoc Conversion", test_pandoc_conversion),
        ("Regex Conversion", test_regex_conversion),
        ("Full Conversion", test_full_conversion),
        ("In-Memory Conversion", test_in_memory_conversion),
        ("CLI Integration", test_cli_integration),
        ("Archive Integration", test_archive_integration),
    ]

    results = []

    for test_name, test_func in tests:
        print(f"\n{'='*20} {test_name} {'='*20}")
        try:
            result = test_func()
            results.append((test_name, result))
        except Exception as e:
            print(f"  EXCEPTION: {e}")
            results.append((test_name, False))

    # Print summary
    print("\n" + "=" * 50)
    print("HTML CONVERSION TEST SUMMARY")
    print("=" * 50)

    passed = sum(1 for _, result in results if result)
    failed = len(results) - passed

    for test_name, result in results:
        status = "PASS" if result else "FAIL"
        print(f"  {test_name}: {status}")

    print(f"\nTotal: {len(results)} tests, Passed: {passed}, Failed: {failed}")

    if failed == 0:
        print("\nAll HTML conversion tests passed!")
    else:
        print(f"\n{failed} test(s) failed")

    return failed == 0


if __name__ == "__main__":
    success = run_html_conversion_tests()
    sys.exit(0 if success else 1)
