def test_regex_conversion():
    """Test regex-based conversion."""
    print("\n=== Testing Regex Conversion ===")

    try:
        from txtarchive.html_converter import convert_html_with_regex
    except ImportError as e:
        print(f"Failed to import html_converter: {e}")
        return False

    html = create_test_html_simple()
    # Strip outer tags for regex converter (it expects content, not full doc)
    import re
    body_match = re.search(r'<body[^>]*>(.*?)</body>', html, flags=re.DOTALL)
    body_html = body_match.group(1) if body_match else html

    result = convert_html_with_regex(body_html)

    checks = [
        ("Has heading", "# Test Document" in result),
        ("Has bold", "**test paragraph**" in result),
        ("Has italic", "*formatted text*" in result),
        ("Has link", "[link](https://example.com)" in result),
        ("Has list items", "- Item one" in result),
        ("Has table", "| Name | Value |" in result),
        ("No HTML tags", "<p>" not in result and "<h1>" not in result),
    ]

    all_passed = True
    for name, passed in checks:
        status = "PASS" if passed else "FAIL"
        print(f"  {status}: {name}")
        if not passed:
            all_passed = False

    return all_passed


